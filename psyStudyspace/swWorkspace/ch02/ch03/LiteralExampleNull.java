package ch03;

//null 값이 없거나 정해지지않은 상태의 값
public class LiteralExampleNull {

	public static void main(String[] args) {
		//null리터럴은 기본타입에는 사용불가
		//int n=null;
		
		//객체타입에 사용가능
		//객체타입 변수 선언 후 값 없이 초기화 할때 사용
		String str=null;
		Object obj=null;
		
		System.out.println(str);
		System.out.println(obj);
		

	}

}
