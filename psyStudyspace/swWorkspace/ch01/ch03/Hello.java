package ch03;
/*
 * 주석은 프로그램의 작성의도나 설명등을 설명하는 용도로 사용되고 컴파일시는 제외됨.
 * */
public class Hello {//class예약어 다음의 문자는 클래스명이다.
	public/*public은 공용의 의미*/ static void main(String[] args) {
		//명령문; - println()함수를 실행(호출)한 명령문;
		System.out.println("Hello!,welcome to the java world");
		/*
		 * 여러줄에 거쳐서 주석을 작성할 수 있습니다.
		 * */
	}//메소드 블럭
}//클래스 블럭